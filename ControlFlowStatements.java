 class ControlFlowStatements
{
    public static void main(String args[])
    {
        int a=99;
        char grade;
        if(a>=90)
        {
            grade = 'A';
        }else if(a>=80)
        {
            grade = 'b';
        }else
        {
            grade = 'f';
        }
    System.out.println(grade);
    }
}
